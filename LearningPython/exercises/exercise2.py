"""
     Elabore um programa que receba quatro notas e calcule a média aritmética entre elas.
"""
grade1 = float(input("Informe a nota 1: "))
grade2 = float(input("Informe a nota 2: "))
grade3 = float(input("Informe a nota 3: "))
grade4 = float(input("Informe a nota 4: "))
avarage = (grade1 + grade2 + grade3 + grade4) / 4

print(f"Sua média aritmética é {avarage}")